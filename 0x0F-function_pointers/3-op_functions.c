/**
 * op_add - sum of a and b
 *
 * @a: first number
 * @b: second number
 *
 * Return: sum of the two numbers
 */

int op_add(int a, int b)
{
	return (a + b); /* adds integers */
}

/**
 * op_sub - difference of a and b
 *
 * @a: first number
 * @b: second number
 *
 * Return: difference of the two numbers
 */

int op_sub(int a, int b)
{
	return (a - b); /* subtracts integers */
}

/**
 * op_mul - product of a and b
 *
 * @a: first number
 * @b: second number
 *
 * Return: product of the two numbers
 */

int op_mul(int a, int b)
{
	return (a * b); /* multiplies integers */
}

/**
 * op_div - divides a by b
 *
 * @a: first number
 * @b: second number
 *
 * Return: result of the division of the two numbers
 */

int op_div(int a, int b)
{
	return (a / b); /* divides integers */
}

/**
 * op_mod - modulus of a and b
 *
 * @a: first number
 * @b: second number
 *
 * Return: remainder of the division of the two numbers
 */

int op_mod(int a, int b)
{
	return (a % b); /* gets the remainder when integers are divided */
}
