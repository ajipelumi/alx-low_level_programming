#include <stdio.h>

/**
 * main - prints the number of arguments passed into it
 *
 * @argc: argument count
 * @argv: argument vector
 *
 * Return: 0
 */

int main(int argc, char __attribute__((unused))*argv[])
{
	int i, n;

	for (i = 0; i < argc; i++)
	{
	n = i;
	}
	printf("%d\n", n);
	return (0);
}
